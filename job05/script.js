function afficherjourssemaines() {
    const jourssemaines = ['Lundi', 'Mardi', 'Mercredi', 'Jeudi', 'Vendredi', 'Samedi', 'Dimanche'];

    // Boucle for pour afficher chaque jour de la semaine
    for (let compteur = 0; compteur < jourssemaines.length; compteur++) {
        console.log(jourssemaines[compteur]); 
    }
}

// Appel de la fonction pour tester son fonctionnement
afficherjourssemaines();
    

